export function getProductById(id: number): Product | undefined {
  return products.find((p) => p.id === id);
}

export function getRelatedProducts(product: Product): Product[] {
  return product.relatedProductIds
    .map((id) => products.find((p) => p.id === id))
    .filter((p): p is Product => p !== undefined);
}

export function getProductsByCategory(category: string): Product[] {
  if (category === 'all') return products;
  return products.filter((p) => p.category === category);
}

export function filterProducts(
  category: string,
  search: string,
  regions: string[],
  certifications: string[],
  minPrice: number,
  maxPrice: number,
  inStockOnly: boolean,
  sort: string,
): Product[] {
  let filtered = [...products];

  if (category !== 'all') {
    filtered = filtered.filter((p) => p.category === category);
  }

  if (search) {
    const s = search.toLowerCase();
    filtered = filtered.filter(
      (p) =>
        p.name.toLowerCase().includes(s) ||
        p.nameEn.toLowerCase().includes(s) ||
        p.producerName.toLowerCase().includes(s) ||
        p.location.toLowerCase().includes(s) ||
        p.category.toLowerCase().includes(s),
    );
  }

  if (regions.length > 0) {
    filtered = filtered.filter((p) => regions.includes(p.region));
  }

  if (certifications.length > 0) {
    filtered = filtered.filter((p) =>
      certifications.some((c) => p.certifications.some((pc) => pc.toLowerCase().includes(c.toLowerCase()))),
    );
  }

  filtered = filtered.filter((p) => p.price >= minPrice && p.price <= maxPrice);

  if (inStockOnly) {
    filtered = filtered.filter((p) => p.stockStatus === 'in_stock');
  }

  switch (sort) {
    case 'price-asc':
      filtered.sort((a, b) => a.price - b.price);
      break;
    case 'price-desc':
      filtered.sort((a, b) => b.price - a.price);
      break;
    case 'rating':
      filtered.sort((a, b) => b.rating - a.rating);
      break;
    default:
      break;
  }

  return filtered;
}
